from __future__ import annotations

import argparse
import json
from pathlib import Path

import pandas as pd


def main() -> None:
    parser = argparse.ArgumentParser(description="Lightweight RC-SBDD leaderboard checker.")
    parser.add_argument("--labels", required=True, help="CSV with target/evidence rows or paper source-data summary.")
    parser.add_argument("--submission", required=True, help="CSV with at least target_key and score columns.")
    parser.add_argument("--out", default="logs/leaderboard_result.json")
    args = parser.parse_args()

    labels = pd.read_csv(args.labels)
    sub = pd.read_csv(args.submission)
    if "target_key" not in sub.columns:
        raise SystemExit("submission must contain a target_key column")
    if "score" not in sub.columns:
        raise SystemExit("submission must contain a score column")

    result = {
        "submission_rows": int(len(sub)),
        "unique_targets": int(sub["target_key"].nunique()),
        "score_mean": float(sub["score"].mean()),
        "score_min": float(sub["score"].min()),
        "score_max": float(sub["score"].max()),
        "label_rows_loaded": int(len(labels)),
        "label_columns": list(labels.columns),
        "note": "This lightweight checker validates submission format. Full benchmark scoring uses oracle_labels.csv generated by the full release builder.",
    }
    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(result, indent=2), encoding="utf-8")
    print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()

